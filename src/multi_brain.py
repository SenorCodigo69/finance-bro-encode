    def _save_vote_record(
        self,
        signals: list[Signal],
        model_decisions: list[tuple[str, list[dict]]],
        final_results: list[tuple[Signal, str, bool]],
    ) -> None:
        """Persist vote details to the database for accuracy tracking.

        Two sets of keys are written:
        - ``vote_record_{batch_ts}`` — full batch record (existing behaviour).
        - ``vote_record_{safe_pair}_{direction}`` — most-recent per-pair record,
          used by :meth:`record_trade_outcome` when given pair+direction.
        """
        batch_ts = now_iso().replace(":", "-")
        record = {
            "timestamp": now_iso(),
            "batch_key": batch_ts,
            "models": [name for name, _ in model_decisions],
            "signals": [],
        }
        for sig, reasoning, approved in final_results:
            sig_record: dict = {
                "pair": sig.pair,
                "direction": sig.direction,
                "strategy": sig.strategy_name,
                "confidence": sig.confidence,
                "consensus": approved,
                "votes": {},
            }
            for model_name, decisions in model_decisions:
                decision_map = {d["pair"]: d for d in decisions if isinstance(d, dict)}
                d = decision_map.get(sig.pair, {})
                sig_record["votes"][model_name] = {
                    "approved": d.get("approved", False),
                    "reasoning": d.get("reasoning", ""),
                }
            record["signals"].append(sig_record)

        try:
            # Full batch record (for historical review)
            self.db.set_state(f"vote_record_{batch_ts}", json.dumps(record))

            # Per-pair shortcut key so record_trade_outcome can look up by pair+direction
            for sig in signals:
                safe_pair = sig.pair.replace("/", "_").replace(":", "_")
                pair_key = f"vote_record_{safe_pair}_{sig.direction}"
                pair_record = {
                    "batch_key": batch_ts,
                    "signals": [s for s in record["signals"] if s["pair"] == sig.pair],
                }
                self.db.set_state(pair_key, json.dumps(pair_record))

        except Exception as e:
            log.debug(f"Failed to save vote record: {e}")

    def _format_trades(self, trades: list[Trade]) -> str:
        if not trades:
            return "No recent trades."
        lines = []
        for t in trades:
            pnl = f"${t.pnl:+.2f}" if t.pnl is not None else "open"
            lines.append(
                f"  {t.direction.upper()} {t.pair} | entry: {t.entry_price:.2f} "
                f"| exit: {t.exit_price or 'open'} | P&L: {pnl} | {t.status} "
                f"| strategy: {t.signal_data.get('strategy', '?')}"
            )
        return "\n".join(lines)

    def _calc_quick_stats(self, trades: list[Trade]) -> dict:
        closed = [t for t in trades if t.pnl is not None]
        if not closed:
            return {"trades": 0}
        wins = [t for t in closed if t.pnl > 0]
        return {
            "trades": len(closed),
            "wins": len(wins),
            "losses": len(closed) - len(wins),
            "win_rate": round(len(wins) / len(closed), 3),
            "total_pnl": round(sum(t.pnl for t in closed), 2),
            "avg_pnl": round(sum(t.pnl for t in closed) / len(closed), 2),
        }

    @property
    def total_tokens_used(self) -> int:
        return sum(p.total_tokens for p in self.providers)
